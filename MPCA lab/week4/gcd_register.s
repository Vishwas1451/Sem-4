//TO FIND SUM OF N DATA ITEMS IN THE MEMORY.STORE THE RESULT
//USING PRE-INDEXING ADDRESSING MODE

.DATA
A: .WORD 8,7 
GCD: .WORD 0

.TEXT
LDR R0,=A
LDR R3,[R0]
ADD R0,R0,#4
LDR R4,[R0]
LDR R1,=GCD


LOOP: CMP R3,R4
      SUBGT R3,R3,R4
      SUBLT R4,R4,R3
      BNE LOOP
STR R3,[R1]
SWI 0X011
    
     	
	

	