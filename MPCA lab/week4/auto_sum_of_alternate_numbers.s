//TO FIND SUM OF N DATA ITEMS IN THE MEMORY.STORE THE RESULY
//USING PRE-INDEXING ADDRESSING WITH WRITE BACK MODE

.DATA
A: .WORD 1,2,3,4
SUM: .WORD 0

.TEXT

MOV R2,#0
LDR R1, =A
LDR R3, =SUM
MOV R5,#8
SUB R1,R1,#8
MOV R4,#1

LOOP: LDR R6,[R1,R5]!
	ADD R2,R2,R6
	ADD R4,R4,#1
	CMP R4,#2
	BNE LOOP
STR R2,[R3]
SWI 0X011
.END